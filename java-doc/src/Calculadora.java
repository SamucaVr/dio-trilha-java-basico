public class Calculadora {
    
    /**
     * Este método é utilizado para calcular dois número inteiros
     * @param numeroUm Este é o primeiro índice do parâmetro
     * @param numeroDois Este é o segundo índice do parâmetro
     * @return Retorna Int, pois o método soma dois número inteiros
     */
    public int somar(int numeroUm, int numeroDois) {
        return numeroUm + numeroDois;
    }
}
